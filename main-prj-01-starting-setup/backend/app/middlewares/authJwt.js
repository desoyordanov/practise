const jwt = require("jsonwebtoken");
const config = require("../config/auth.config.js");
const db = require("../models");
const User = db.user;
const Role = db.role;
const { FB } = require("fb");
const jwt_decode = require("jwt-decode");

verifyToken = (req, res, next) => {
  let token = req.headers["x-access-token"];

  if (!token) {
    return res.status(403).send({ message: "No token provided!" });
  }

  jwt.verify(token, config.secret, (err, decoded) => {
    if (err) {
      return res.status(401).send({ message: "Unauthorized!" });
    }
    req.userId = decoded.id;
    next();
  });
};

decodeAuthToken = (req, res, next) => {
  console.log(req.body);
  if (req.body.type === "gg") {
    const decodedToken = jwt_decode(req.body.accessToken);
    req.body.ku1 = decodedToken.name;
    req.body.ku2 = decodedToken.email;
    next();
  } else if (req.body.type === "fb") {
    FB.api(
      "me",
      { fields: ["name", "email"], access_token: req.body.accessToken },
      function (res) {
        console.log(res);
        req.body.kur1 = res.email;
        req.body.kur2 = res.name;
        next();
      }
    );
  }
  next();
};
isAdmin = (req, res, next) => {
  User.findById(req.userId).exec((err, user) => {
    if (err) {
      res.status(500).send({ message: err });
      return;
    }

    Role.find(
      {
        _id: { $in: user.roles },
      },
      (err, roles) => {
        if (err) {
          res.status(500).send({ message: err });
          return;
        }

        for (let i = 0; i < roles.length; i++) {
          if (roles[i].name === "admin") {
            next();
            return;
          }
        }

        res.status(403).send({ message: "Require Admin Role!" });
        return;
      }
    );
  });
};

